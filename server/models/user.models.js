import mongoose from "mongoose";

const sampleSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  mobile: {
    type: Number,
    required: true,
  },
});

const UserModel = mongoose.model("Sample", sampleSchema);
export default UserModel;
